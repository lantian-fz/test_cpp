//力扣 414. 第三大的数
//给定一个非空数组，返回此数组中第三大的数。如果不存在，则返回数组中最大的数。要求算法时间复杂度必须是O(n)。
//输入: [3, 2, 1]
//输出: 1
//解释: 第三大的数是 1.
//
//输入: [1, 2]
//输出: 2
//解释: 第三大的数不存在, 所以返回最大的数 2 .
//
//输入: [2, 2, 3, 1]
//输出: 1
//解释: 注意，要求返回第三大的数，是指第三大且唯一出现的数。存在两个值为2的数，它们都排第二。

#include <iostream>
#include <set>
#include <vector>
using namespace std;

int thirdMax(vector<int> &nums)
{
	set<int> st(nums.begin(),nums.end());
	auto it = st.end();
	if (st.size() < 3)
		return *(--it);
	else
		return *(------it);
}

int main()
{
	vector<int> nums = { 2, 1};

	cout << thirdMax(nums) << endl;

	return 0;
}